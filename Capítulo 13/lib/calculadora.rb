# frozen_string_literal: true

# Classe que efetua as operacoes basicas
class Calculadora
  def self.soma(numero1, numero2)
    numero1 + numero2
  end

  def self.subtrai(numero1, numero2)
    numero1 - numero2
  end

  def self.multiplica(numero1, numero2)
    numero1 * numero2
  end

  def self.divide(numero1, numero2)
    numero1 / numero2
  end
end
